const HANGUL_OFFSET = 0xac00;

export function decomposeHangul(str: string): string {
  let result = "";
  for (const char of str) {
    const code = char.charCodeAt(0);

    if (code >= HANGUL_OFFSET && code <= 0xd7a3) {
      const index = code - HANGUL_OFFSET;
      const cho = Math.floor(index / (JUNG.length * JONG.length));
      const jung = Math.floor(
        (index % (JUNG.length * JONG.length)) / JONG.length,
      );
      const jong = index % JONG.length;

      result += CHO[cho] + JUNG[jung];
      if (jong > 0) {
        result += JONG[jong];
      }
    } else {
      result += char;
    }
  }
  return result;
}
